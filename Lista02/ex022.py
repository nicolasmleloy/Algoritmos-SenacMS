num = int(input("Informe um número entre 1 e 7: "))

if(num < 1) or (num > 7):
    print("Número inválido!")
elif(num == 1):
    print("Domingo!")
elif(num == 2):
    print("Segunda!")
elif(num == 3):
    print("Terça!")
elif(num == 4):
    print("Quarta!")
elif(num == 5):
    print("Quinta!")
elif(num == 6):
    print("Sexta!")
elif(num == 7):
    print("Sábado!")